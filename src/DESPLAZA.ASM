.MODEL SMALL
.STACK
.DATA
	M1 DB 10, 13, "Sed ut perspiciatis unde omnis iste natus error sit voluptatem accusantium doloremque laudantium, totam rem aperiam, eaque ipsa quae ab illo inventore veritatis et quasi architecto beatae vitae dicta sunt explicabo. Nemo enim ipsam voluptatem =)","$"
	M2 DB 10, 13, "EN EPOCA DE LOS EMPERADORES AURELIANO, TACITO Y PROBO, UN BANDIDO DE NOMBRE LIDIO, GOZO DE TEMIBLE RENOMBRE EN LAS PROVINCIAS DEL ASIA MENOR, ISAURIA, PISIDIA Y LICIA, TRISTEMENTE CELEBRES A CAUSA DE LOS NIDOS DE xD", "$"
.CODE
	MOV AX,@DATA
	MOV DS,AX
	MOV AH,09H
	LEA DX, M1
	INT 21H
	
	CALL DESPLAZA_PANTALLA
	
	LEA DX, M2
	INT 21H
.EXIT
PUBLIC DESPLAZA_PANTALLA
DESPLAZA_PANTALLA PROC
	;SALVACIONES
	PUSH AX
	PUSH BX
	PUSH CX
	PUSH DX
	;DESPLAZAR PANTALLA
	MOV AH, 6H;SERVICIO 6
	MOV AL, 0;EL CERO AQUI SIGNIFICA QUE TODA EL AREA SE DESPLAZA
	;LAS DOS PARTES DE CX INDICAN LAS COORDENADAS DE P1
	MOV CH, 0
	MOV CL, 0
	;LAS DOS PARTES DE DX INDICAN LAS COORDENADAS DE P2 JAJA
	MOV DH, 1
	MOV DL, 1
	COMMENT !
	HAY QUE RECORDAR QUE LAS COORDENADAS TIENEN ORIGEN EN LA ESQUINA 
	SUPERIOR IZQUIERDA DE LA PANTALLA Y QUE EL SEMIEJE HORIZONTAL POSITIVO
	SE EXTIENDE HACIA LA DERECHA, Y EL SEMIEJE VERTICAL POSITIVO SE EXTIENDE 
	HACIA ABAJO.
	!
	MOV BH, 0H;ESTO HARA QUE EL FONDO Y EL PRIMER PLANO SEAN NEGROS
	COMMENT !
	BH ESTA CONFORMADO POR 8 BITS. LOS 4 BITS MAS ALTOS DEFINEN EL COLOR
	DE FONDO; LOS MAS BAJOS DEFINEN EL COLOR DEL PRIMER PLANO.
	!
	INT 10H
	;RESTAURACIONES
	POP DX
	POP CX
	POP BX
	POP AX
	RET
DESPLAZA_PANTALLA ENDP
END