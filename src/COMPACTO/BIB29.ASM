COMMENT !
	EL NUMERO DECIMAL MAS GRANDE DEBE TENER 4 DIGITOS
	ENTONCES LA MAXIMA CANTIDAD DE DIGITOS DE CADA BASE ESCRIBE
	HEX: 4
	OCT: 5
	BIN: 16
!
.MODEL SMALL
.STACK
.DATA
.CODE
PUBLIC IMPRIME_CARACTER
IMPRIME_CARACTER PROC
COMMENT !
	ESTE PROCEDIMIENTO IMPRIME EL CARACTER CUYO CODIGO ASCII ESTE GUARDADO EN DL
!
	PUSH AX
	MOV AH, 02
	INT 21H
	POP AX
	RET
IMPRIME_CARACTER ENDP
PUBLIC LINEA_GENERAL
LINEA_GENERAL PROC
COMMENT !
	ESTE PROCEDIMIENTO REQUIERE QUE EN CX ESTE GUARDADA LA LONGITUD DE LA LINEA HORIZONTAL
	REQUIERE QUE DL CONTENGA EL CODIGO ASCII DEL CARACTER A IMPRIMIR EN LINEA
!
	PUSH CX
	PUSH DX
	CMP CX, 0
	JE NO_HAGAS_NADA
CICLO:
	CALL IMPRIME_CARACTER
	LOOP CICLO
NO_HAGAS_NADA:
	NOP
	POP DX
	POP CX
	RET
LINEA_GENERAL ENDP
PUBLIC CARGAR_DATOS
CARGAR_DATOS PROC
	PUSH AX
	MOV AX, @DATA
	MOV DS, AX
	POP AX
	RET
CARGAR_DATOS ENDP
PUBLIC IMPRIME_CADENA
IMPRIME_CADENA PROC
	;DS:DX DEBE APUNTAR A UNA CADENA QUE TERMINE EN '$'
	PUSH AX
	MOV AH, 09H
	INT 21H
	POP AX
	RET
IMPRIME_CADENA ENDP
PUBLIC DALE_ENTER
DALE_ENTER PROC
	PUSH DX
	MOV DL, 10
	CALL IMPRIME_CARACTER
	MOV DL, 13
	CALL IMPRIME_CARACTER
	POP DX
	RET
DALE_ENTER ENDP
PUBLIC LEE_CARACTER;NO HACE FALTA PUSH AX
LEE_CARACTER PROC
	COMMENT !
		ESTE PROCEDIMIENTO LEE UN CARACTER DESDE TECLADO
		Y GUARDA SU CODIGO ASCII EN AL. EL CARACTER LEIDO A SU VEZ
		SE DEJA ESCRITO EN LA PANTALLA
	!
	MOV AX, 0100H
	INT 21H
	RET
LEE_CARACTER ENDP
PUBLIC LEE_1DECIMAL
LEE_1DECIMAL PROC 
	CALL LEE_CARACTER
	SUB AL, "0"
	MOV AH, 0H
	RET
LEE_1DECIMAL ENDP
PUBLIC PRODUCTO
PRODUCTO PROC
	COMMENT !
		ESTE PROCEDIMIENTO ESPERA UN DATO POR EL CUAL MULTIPLICAR A AX (MAS BIEN A AL) QUE ESTE ALMACENADO EN BX
	!
	PUSH BX
	MOV AH, 0H
	MUL BX
	POP BX
	RET
PRODUCTO ENDP
PUBLIC LEER_SIN_ECO
LEER_SIN_ECO PROC
	MOV AH, 08H
	INT 21H
	RET
LEER_SIN_ECO ENDP
.EXIT
